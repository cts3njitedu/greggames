package com.craig.cards;

public class TempWinner {

	private String tempWinnerCard;
	
	private String currPlayerCard;

	
	
	private int trickCount;




	public String getTempWinnerCard() {
		return tempWinnerCard;
	}




	public void setTempWinnerCard(String tempWinnerCard) {
		this.tempWinnerCard = tempWinnerCard;
	}




	public String getCurrPlayerCard() {
		return currPlayerCard;
	}




	public void setCurrPlayerCard(String currPlayerCard) {
		this.currPlayerCard = currPlayerCard;
	}




	public int getTrickCount() {
		return trickCount;
	}




	public void setTrickCount(int trickCount) {
		this.trickCount = trickCount;
	}

}
